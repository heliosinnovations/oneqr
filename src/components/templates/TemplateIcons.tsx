// Get icon component by template ID
export function getTemplateIcon(
  templateId: TemplateId
): React.ComponentType<IconProps> {
  const iconMap: Record<TemplateId, React.ComponentType<IconProps>> = {
    wifi: WifiIcon,
    vcard: VCardIcon,
    whatsapp: WhatsAppIcon,
    email: EmailIcon,
    sms: SMSIcon,
    phone: PhoneIcon,
    "app-store": AppStoreIcon,
    maps: MapsIcon,
    calendar: CalendarIcon,
    payment: PaymentIcon,
  };

  return iconMap[templateId] || QRCodeIcon;
}

